    private void Receiver_NameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_Receiver_NameKeyPressed
        // TODO add your handling code here:
        char x = evt.getKeyChar();
        if (Character.isLetter(x) || Character.isWhitespace(x) || Character.isISOControl(x)){
           Receiver_Name.setEditable(true);
        }else {
            Receiver_Name.setEditable(false);
        }
    }
