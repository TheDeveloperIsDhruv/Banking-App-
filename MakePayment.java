    private void MakePayment_EnterAmtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_MakePayment_EnterAmtKeyPressed
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (Character.isLetter(c)){
           MakePayment_EnterAmt.setEditable(false);
        }else {
            MakePayment_EnterAmt.setEditable(true);
        }

    }//GEN-LAST:event_MakePayment_EnterAmtKeyPressed

